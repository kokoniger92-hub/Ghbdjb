    if "message" in данные:
        сообщение = данные["message"]
        юзер = сообщение["from"]["id"]
        чат = сообщение["chat"]["id"]
        текст = сообщение.get("text")
        имя = сообщение["from"].get("first_name", "")
        юзернейм = сообщение["from"].get("username", "")
        
        # Регистрация воркера (если админ добавил ID в список)
        if юзер in WORKERS and юзер not in воркеры:
            воркеры[юзер] = {"status": "free", "current_ticket": None, "name": имя, "username": юзернейм}
            отправить(юзерам_админа, f"✅ Воркер @{юзернейм or имя} зарегистрирован")
        
        # Команда /start
        if текст == "/start":
            if юзер in WORKERS:
                отправить(чат, "👋 Привет, воркер! Жди заявки.", кнопки_админу() if юзер == ADMIN else {"keyboard": [[{"text":"📋 Статус"}]],"resize_keyboard":True})
